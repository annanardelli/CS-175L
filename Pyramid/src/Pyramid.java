
public class Pyramid {

	public static void main (String []args) {
		for (int i=1; i<=1;  i++) {
			System.out.println("    .  ");
			for (int j=1; j<=1; j++) {
				System.out.println("   ... ");
				for(int l=1; l<=1; l++) {
					System.out.println("  .....");
					for(int h=1; h<=1; h++) {
						System.out.println(" .......");
							for(int a=1; a<=1; a++) {
								System.out.println(".........");
							}
				}
			}
		}
		

	}
}
}
